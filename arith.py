first = int(input("첫 번째 정수 : "))
second = int(input("두 번째 정수 : "))

# 파이썬의 산술 연산자들
# 산술 연산자 : 수와 수를 연산해서 결과가 수인 연산
print("{} + {} = {}".format(first, second, (first + second)))
print("{} - {} = {}".format(first, second, (first - second)))
print("{} * {} = {}".format(first, second, (first * second)))
print("{} / {} = {}".format(first, second, (first / second)))
print("{} // {} = {}".format(first, second, (first // second)))
print("{} % {} = {}".format(first, second, (first % second)))
print("{} ** {} = {}".format(first, second, (first ** second)))


